-- Rank validation helper
function PLUGIN:IsValidRank(division, rankStr)
    if not rankTable[division] then return false end

    for _, rankData in ipairs(rankTable[division]) do
        if rankData.rank == rankStr or rankData.fullRank == rankStr then
            return true
        end
    end

    return false
end

-- Get next rank helper
function PLUGIN:GetNextRank(character)
    if not character or not factionDivisionMap[character:GetFaction()] then return nil end

    local rankinfo = character:GetData("rankinfo")
    if not rankinfo then return nil end

    local nextPaygrade  = rankinfo.paygrade + 1
    local divisionRanks = rankTable[rankinfo.division]

    if divisionRanks and divisionRanks[nextPaygrade] then
        return divisionRanks[nextPaygrade]
    end

    return nil
end

-- Get previous rank helper
function PLUGIN:GetPreviousRank(character)
    if not character or not factionDivisionMap[character:GetFaction()] then return nil end

    local rankinfo = character:GetData("rankinfo")
    if not rankinfo then return nil end

    local prevPaygrade  = rankinfo.paygrade - 1
    local divisionRanks = rankTable[rankinfo.division]

    if divisionRanks and divisionRanks[prevPaygrade] and prevPaygrade > 0 then
        return divisionRanks[prevPaygrade]
    end

    return nil
end